    public static ArrayList<double[]> combineArrays(ArrayList<double[]> arrayOfArrays) {
        HashMap<String, Double> map = new HashMap<>();
        for (double[] subArray : arrayOfArrays) {
            String key = subArray[0] + "," + subArray[1] + "," + subArray[2] + "," + subArray[3];
            if (map.containsKey(key)) {
                double sum = map.get(key) + subArray[4];
                map.put(key, sum);
            } else {
                map.put(key, subArray[4]);
            }
        }

        ArrayList<double[]> combinedArray = new ArrayList<>();
        for (String key : map.keySet()) {
            double[] combinedSubArray = new double[5];
            String[] keyParts = key.split(",");
            for (int i = 0; i < keyParts.length; i++) {
                combinedSubArray[i] = Double.parseDouble(keyParts[i]);
            }
            combinedSubArray[4] = map.get(key);
            combinedArray.add(combinedSubArray);
        }
        return combinedArray;
    }

