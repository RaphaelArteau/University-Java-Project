    public static void saveToCsv(ArrayList<double[]> uncombinedArrayList) {
        ArrayList<double[]> arrayList = combineArrays(uncombinedArrayList);
        // Choose a file location using file explorer
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Sauvegarder fichier CSV");
        int userSelection = fileChooser.showSaveDialog(null);
        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();

            String fileName = fileToSave.getAbsolutePath();
            if(!fileName.endsWith(".csv")){
                fileName = fileName+".csv";
            }

            // Write the ArrayList of arrays to a CSV file
            try (FileWriter writer = new FileWriter(fileName)) {
                // Write header row
                writer.write("Dimension,Length,Left Cut,Right Cut,Quantity\n");
                
                DecimalFormat df = new DecimalFormat("#.##");
                
                // Write data rows
                for (double[] array : arrayList) {
                    if (array[4] != 0){
                        StringBuilder sb = new StringBuilder();
                        String typeBois = String.valueOf((int) array[0]).substring(0, 1) + "x" + String.valueOf((int) array[0]).substring(1);
                        sb.append(typeBois).append(",");
                        sb.append(fractionator(array[1])).append(",");
                        sb.append(df.format(array[2])).append(",");
                        sb.append(df.format(array[3])).append(",");
                        sb.append((int) array[4]).append("\n");
                        writer.write(sb.toString());
                    }
                }
            } catch (IOException e) {
                System.err.format("IOException: %s%n", e);
            }
        }
    }
    
    
